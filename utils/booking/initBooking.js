export const preparedRoundTripBooking = (
  airSolution,
  bookingReq,
  count = 0
) => {
  if (airSolution) {
    if (bookingReq) {
      mappSolutionSegment(airSolution, bookingReq);

      mappAirPriceInf(
        airSolution.airPriceOpt &&
          airSolution.airPriceOpt &&
          airSolution.airPriceOpt.airPricingInfo,
        bookingReq
      );

      mapAirBookingPriceInfo(
        bookingReq,
        airSolution && airSolution.airPriceOpt,
        count
      );
      return true;
    }
  }
  return false;
};

